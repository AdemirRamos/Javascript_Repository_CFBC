//Aula 09 - Diferença Entre Pré-incremento e Pós-incremento

let n = 10

console.log(n++) //Pós-incremento.
console.log(++n) //Pré-incremento.

n += 10

console.log(n)

console.log(n--) //Pós-incremento.
console.log(--n) //Pré-incremento.

