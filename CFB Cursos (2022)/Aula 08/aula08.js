//Aula 08 - Operadores Bitwise em Javascript

//Para mais detalhes, ver aula 08.

let n1 = 10
let n2 = 11
let resultado

resultado = n1 & n2

console.log(resultado)

resultado = n1 | n2

console.log(resultado)

resultado = n1 ^ n2

console.log(resultado)

let n3 = 10

resultado = n3 << 1

console.log(resultado)

resultado = n3 >> 1

console.log(resultado)

resultado = n3 << 2

console.log(resultado)
